class EventDetails {
  final int id;
  final String name;
  final String location;
  final String date;
  final double price;
  final double rating;
  final String description;
  final String image;
  final String event;

  EventDetails({
    required this.id,
    required this.name,
    required this.location,
    required this.date,
    required this.price,
    required this.rating,
    required this.description,
    required this.image,
    required this.event,
  });

}
